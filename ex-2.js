const add = function (a, b) {
  return a + b;
};

const subtract = function (a, b) {
  return a - b;
};

const divide = function (a, b) {
  return a / b;
};

const multiply = function (a, b) {
  return a * b;
};

//answer
// a+b 
let result1 = add(10, 5);
console.log(result1);
// a-b
let result2 = subtract(10, 5);
console.log(result2);
// a*b
let result3 = multiply(10, 5);
console.log(result3);
// a/b
let result4 = divide(10, 5);
console.log(result4);